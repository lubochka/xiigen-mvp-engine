// ── Mock pass executor (S3 refactor: AfPipeline delegates to GenericNodeExecutor) ──

function makePassExecutor(): GenericNodeExecutor {
  return {
    execute: jest.fn(async () =>
      DataProcessResult.success({
        runId: 'e2e-run-id',
        status: 'PASS',
        score: 90,
        trace: [
          { nodeId: 'rag-retrieve', nodeType: 'rag-retrieve', status: 'PASS', durationMs: 10 },
          { nodeId: 'ai-generate', nodeType: 'ai-generate', status: 'PASS', durationMs: 250 },
          { nodeId: 'validate', nodeType: 'validate', status: 'PASS', durationMs: 5 },
        ],
        finalOutput: { code: '// generated by e2e test executor' },
        promoted: true,
        promotionLevel: 'MINIMAL',
      }),
    ),
    getTrace: jest.fn(async () => DataProcessResult.success(null)),
  } as unknown as GenericNodeExecutor;
}


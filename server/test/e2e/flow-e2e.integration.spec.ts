describe('Flow E2E — DataProcessResult Contract (DNA-3)', () => {
  it('FlowGenerator.generate() never throws — failures wrapped in DataProcessResult', async () => {
    // Invalid contract (empty taskTypeId) → should return DataProcessResult.failure
    const invalidContract = new EngineContract({
      taskTypeId: '',
      name: 'Invalid',
      archetype: ContractArchetype.DATA_PIPELINE,
      entry: '',
      purpose: '',
      factoryDependencies: [],
      afStations: [],
      qualityGates: [],
      bfaRegistration: { entities: [], events: [], apiRoutes: [] },
      ironRules: [],
      machineComponents: [],
      freedomComponents: [],
      familyId: '',
    });

    const engine = createEngine();
    const result = await engine.generate(invalidContract, 'nothrow-tenant');

    expect(result).toBeInstanceOf(DataProcessResult);
    // May succeed with warnings or fail — but never throws
    // Either isSuccess=true (with error warnings) or isSuccess=false with errorCode
    if (!result.isSuccess) {
      expect(result.errorCode).toBeDefined();
    }
  });
});

def run(cmd: list[str], *, cwd: Path, env: dict[str, str] | None = None) -> tuple[int, str]:
    """Run a subprocess, capture stdout+stderr, return (exit_code, output)."""
    print(f"\n$ {' '.join(cmd)}    (cwd={cwd})", flush=True)
    p = subprocess.Popen(
        cmd,
        cwd=str(cwd),
        env=env,
        stdout=subprocess.PIPE,
        stderr=subprocess.STDOUT,
        text=True,
        encoding="utf-8",
        errors="replace",
        shell=False,
    )
    chunks: list[str] = []
    assert p.stdout is not None
    for line in iter(p.stdout.readline, ""):
        chunks.append(line)
        sys.stdout.write(line)
        sys.stdout.flush()
    p.wait()
    return p.returncode, "".join(chunks)


def main() -> int:
    sys.stdout.reconfigure(encoding="utf-8")  # type: ignore[attr-defined]
    parser = argparse.ArgumentParser()
    parser.add_argument("--tmp-dir", default=None, help="Custom tmp dir (default: system tmp)")
    parser.add_argument("--keep", action="store_true", help="Don't clean up the fork tmp dir on exit")
    parser.add_argument("--label", default=None, help="Custom suffix for the fork dir name")
    args = parser.parse_args()

    label = args.label or f"flow-01-fork-{int(time.time())}"
    if args.tmp_dir:
        fork = Path(args.tmp_dir).resolve() / label
        fork.mkdir(parents=True, exist_ok=True)
    else:
        fork = Path(tempfile.mkdtemp(prefix=f"xiigen-{label}-"))

    print("=" * 78)
    print(f"FLOW-01 fork-and-install harness")
    print(f"  source : {SOURCE_SERVER}")
    print(f"  fork   : {fork}")
    print("=" * 78)

    # Step 0 — populate the fork tree
    try:
        for src_rel, dst_rel in COPY_TREES:
            src = SOURCE_SERVER / src_rel
            dst = fork / dst_rel
            dst.parent.mkdir(parents=True, exist_ok=True)
            if dst.exists():
                shutil.rmtree(dst)
            shutil.copytree(src, dst, ignore=_copy_filter)
            file_count = sum(1 for _ in dst.rglob("*.ts"))
            print(f"  copied {src_rel:55s} -> {dst_rel:55s} ({file_count} .ts files)")

        # Generated metadata
        (fork / "package.json").write_text(FORK_PACKAGE_JSON, encoding="utf-8")
        (fork / "tsconfig.json").write_text(FORK_TSCONFIG_JSON, encoding="utf-8")
        (fork / "jest.config.js").write_text(FORK_JEST_CONFIG, encoding="utf-8")
        (fork / "__tests__").mkdir(exist_ok=True)
        (fork / "__tests__" / "fork-smoke.spec.ts").write_text(FORK_SMOKE_SPEC, encoding="utf-8")
        print(f"  wrote  package.json + tsconfig.json + jest.config.js + __tests__/fork-smoke.spec.ts")
    except Exception as exc:
        print(f"\n[harness] setup failed: {exc!r}")
        return 4

    env = os.environ.copy()
    env["CI"] = "1"
    env.setdefault("npm_config_loglevel", "warn")

    npm = "npm.cmd" if os.name == "nt" else "npm"
    npx = "npx.cmd" if os.name == "nt" else "npx"

    # Step 1 — npm install
    rc, _ = run([npm, "install", "--no-audit", "--no-fund"], cwd=fork, env=env)
    if rc != 0:
        print("\n[harness] STEP 1 FAILED: npm install non-zero exit code", rc)
        if not args.keep:
            shutil.rmtree(fork, ignore_errors=True)
        return 1

    # Step 2 — npx tsc --noEmit
    rc, _ = run([npx, "tsc", "--noEmit"], cwd=fork, env=env)
    if rc != 0:
        print("\n[harness] STEP 2 FAILED: tsc reported errors")
        if not args.keep:
            print(f"  fork preserved at: {fork} for inspection")
            args.keep = True
        return 2

    # Step 3 — npx jest
    rc, _ = run([npx, "jest", "--colors=false"], cwd=fork, env=env)
    if rc != 0:
        print("\n[harness] STEP 3 FAILED: jest reported failures")
        if not args.keep:
            print(f"  fork preserved at: {fork} for inspection")
            args.keep = True
        return 3

    print("\n" + "=" * 78)
    print("FLOW-01 fork-and-install harness: ALL GREEN")
    print("  npm install : OK")
    print("  tsc --noEmit: OK")
    print("  jest        : OK")
    print("=" * 78)

    if not args.keep:
        try:
            shutil.rmtree(fork, ignore_errors=True)
            print(f"  cleaned up tmp fork dir: {fork}")
        except Exception:
            print(f"  could not remove tmp fork dir at {fork}; leaving in place")

    return 0


if __name__ == "__main__":
    sys.exit(main())

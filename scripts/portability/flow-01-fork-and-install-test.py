# Filter copied trees: skip test files and IDE caches. Production source only.
def _copy_filter(src: str, names: list[str]) -> list[str]:
    skip = []
    for n in names:
        if n.startswith(".") or n == "__pycache__" or n == "node_modules":
            skip.append(n)
        elif n.endswith(".spec.ts") or n.endswith(".test.ts"):
            skip.append(n)
        elif n == "__tests__":
            skip.append(n)
        elif n.endswith(".tsbuildinfo"):
            skip.append(n)
    return skip


